std::unique_ptr<EmulatorWindow> EmulatorWindow::Create(
    Emulator* emulator, ui::WindowedAppContext& app_context, uint32_t width,
    uint32_t height, bool is_game_process) {
  assert_true(app_context.IsInUIThread());
  std::unique_ptr<EmulatorWindow> emulator_window(new EmulatorWindow(
      emulator, app_context, width, height, is_game_process));
  if (!emulator_window->Initialize()) {
    return nullptr;
  }
  return emulator_window;
}

EmulatorWindow::~EmulatorWindow() {
  // Kill all child processes when the parent window is destroyed
#if XE_PLATFORM_WIN32
  for (HANDLE process : child_processes_) {
    DWORD exit_code;
    // Check if process is still running
    if (GetExitCodeProcess(process, &exit_code) && exit_code == STILL_ACTIVE) {
      // Terminate the process
      TerminateProcess(process, 0);
      // Wait briefly for it to exit
      WaitForSingleObject(process, 1000);
    }
    // Close the handle regardless
    CloseHandle(process);
  }
#else
  for (pid_t pid : child_processes_) {
    // Check if process still exists before trying to kill it
    if (kill(pid, 0) == 0) {
      // Process exists, send SIGTERM to gracefully terminate
      kill(pid, SIGTERM);
      // Give it a moment to exit gracefully
      usleep(100000);  // 100ms
      // If still running, force kill
      kill(pid, SIGKILL);
    }
    // Try to reap it if it's a zombie
    int status;
    waitpid(pid, &status, WNOHANG);
  }
#endif
  child_processes_.clear();

  // Notify the ImGui drawer that the immediate drawer is being destroyed.
  ShutdownGraphicsSystemPresenterPainting();
}

ui::Presenter* EmulatorWindow::GetGraphicsSystemPresenter() const {
  gpu::GraphicsSystem* graphics_system = emulator_->graphics_system();
  return graphics_system ? graphics_system->presenter() : nullptr;
}

void EmulatorWindow::SetupGraphicsSystemPresenterPainting() {
  ShutdownGraphicsSystemPresenterPainting();

  if (!window_) {
    return;
  }

  ui::Presenter* presenter = GetGraphicsSystemPresenter();
  if (!presenter) {
    return;
  }

  ApplyDisplayConfigForCvars();

  window_->SetPresenter(presenter);

  immediate_drawer_ =
      emulator_->graphics_system()->provider()->CreateImmediateDrawer();
  if (immediate_drawer_) {
    immediate_drawer_->SetPresenter(presenter);
    imgui_drawer_->SetPresenterAndImmediateDrawer(presenter,
                                                  immediate_drawer_.get());
    Profiler::SetUserIO(kZOrderProfiler, window_.get(), presenter,
                        immediate_drawer_.get());
  }
}

void EmulatorWindow::ShutdownGraphicsSystemPresenterPainting() {
  Profiler::SetUserIO(kZOrderProfiler, window_.get(), nullptr, nullptr);
  imgui_drawer_->SetPresenterAndImmediateDrawer(nullptr, nullptr);
  immediate_drawer_.reset();
  if (window_) {
    window_->SetPresenter(nullptr);
  }
}

void EmulatorWindow::OnEmulatorInitialized() {
  // For UI process: emulator is not initialized, just show Qt UI
  if (!is_game_process_) {
    auto* qt_window = dynamic_cast<ui::QtWindow*>(window_.get());
    if (qt_window) {
      // Get the central widget and its layout
      QWidget* central_widget = qt_window->qwindow()->centralWidget();
      if (central_widget && central_widget->layout()) {
        game_list_dialog_qt_ = new GameListDialogQt(central_widget, this);
        // Add it to the layout so it fills the window
        central_widget->layout()->addWidget(game_list_dialog_qt_);
      }
    }

    // Start periodic child process checking for UI process
    ScheduleChildProcessCheck();

    emulator_initialized_ = true;
    window_->SetMainMenuEnabled(true);
    return;
  }

  // Game process: emulator is fully initialized
  if (!emulator_->kernel_state()
           ->xam_state()
           ->profile_manager()
           ->GetAccountCount()) {
    new NoProfileDialog(imgui_drawer_.get(), this);
    disable_hotkeys_ = true;
  }

  // Game process: enable cursor auto-hide
  window_->SetCursorVisibility(ui::Window::CursorVisibility::kAutoHidden);

  emulator_initialized_ = true;
  window_->SetMainMenuEnabled(true);
  // When the user can see that the emulator isn't initializing anymore (the
  // menu isn't disabled), enter fullscreen if requested.
  if (cvars::fullscreen) {
    SetFullscreen(true);
  }

  if (IsUseNexusForGameBarEnabled()) {
    XELOGE(
        "Xbox Gamebar Enabled, using BACK button instead of GUIDE for "
        "controller hotkeys!!!");
  }

  // Create a thread to listen for controller hotkeys.
  if (cvars::controller_hotkeys) {
    Gamepad_HotKeys_Listener =
        threading::Thread::Create({}, [&] { GamepadHotKeys(); });
    Gamepad_HotKeys_Listener->set_name("Gamepad HotKeys Listener");
  }

  // Register callback for launch data restart requests from the kernel
  auto* qt_window = dynamic_cast<ui::QtWindow*>(window_.get());
  if (qt_window) {
    emulator_->set_on_launch_data_restart([this, qt_window]() {
      // Show dialog in UI thread
      window_->app_context().CallInUIThread([this, qt_window]() {
        auto* dialog = new app::LaunchDataRestartDialogQt(
            qt_window->qwindow(), window_.get(), emulator_->kernel_state());
        dialog->show();
      });
    });
  }
}

void EmulatorWindow::EmulatorWindowListener::OnClosing(ui::UIEvent& e) {
  // Game process: exit immediately without cleanup to avoid Vulkan hangs
  if (emulator_window_.is_game_process_) {
    std::quick_exit(0);
  }

  // UI process: kill all child processes when the parent window is closing
#if XE_PLATFORM_WIN32
  for (HANDLE process : emulator_window_.child_processes_) {
    DWORD exit_code;
    if (GetExitCodeProcess(process, &exit_code)) {
      if (exit_code == STILL_ACTIVE) {
        XELOGI("Terminating child process");
        TerminateProcess(process, 0);
        // Don't wait, just close handle
      }
    }
    CloseHandle(process);
  }
#else
  for (pid_t pid : emulator_window_.child_processes_) {
    // Check if process is still alive
    if (kill(pid, 0) == 0) {
      XELOGI("Terminating child process {}", pid);
      // Send SIGKILL directly for immediate termination
      kill(pid, SIGKILL);
    }
  }
#endif

  emulator_window_.child_processes_.clear();
  emulator_window_.app_context_.QuitFromUIThread();
}


void EmulatorWindow::OnKeyDown(ui::KeyEvent& e) {
  if (!emulator_initialized_) {
    return;
  }

  // If we're the UI process and have a child process running, forward certain
  // keys
  if (!is_game_process_ && HasRunningChildProcess()) {
    // Forward keys that should be handled by the game process
    switch (e.virtual_key()) {
      case ui::VirtualKey::kF11:
      case ui::VirtualKey::kF12:
      case ui::VirtualKey::kMultiply:  // Numpad *
      case ui::VirtualKey::kSubtract:  // Numpad -
      case ui::VirtualKey::kAdd:       // Numpad +
      case ui::VirtualKey::kF3:
      case ui::VirtualKey::kF4:
      case ui::VirtualKey::kF5:
      case ui::VirtualKey::kF6:
        SendKeyToChild(e.virtual_key(), e.is_ctrl_pressed(), e.is_alt_pressed(),
                       e.is_shift_pressed());
        e.set_handled(true);
        return;
      case ui::VirtualKey::kPause:
        if (e.is_ctrl_pressed() && e.is_shift_pressed()) {
          SendKeyToChild(e.virtual_key(), e.is_ctrl_pressed(),
                         e.is_alt_pressed(), e.is_shift_pressed());
          e.set_handled(true);
          return;
        }
        break;
      default:
        // Let other keys fall through to be handled locally
        break;
    }
  }

  switch (e.virtual_key()) {
    case ui::VirtualKey::kO: {
      if (!e.is_ctrl_pressed()) {
        return;
      }
      FileOpen();
    } break;
    case ui::VirtualKey::kMultiply: {
      CpuTimeScalarReset();
    } break;
    case ui::VirtualKey::kSubtract: {
      CpuTimeScalarSetHalf();
    } break;
    case ui::VirtualKey::kAdd: {
      CpuTimeScalarSetDouble();
    } break;

    case ui::VirtualKey::kF3: {
      Profiler::ToggleDisplay();
    } break;

    case ui::VirtualKey::kF4: {
      GpuTraceFrame();
    } break;
    case ui::VirtualKey::kF5: {
      GpuClearCaches();
    } break;

    case ui::VirtualKey::kF6: {
      ToggleDisplayConfigDialog();
    } break;
    case ui::VirtualKey::kF11: {
      ToggleFullscreen();
    } break;
    case ui::VirtualKey::kF12: {
      TakeScreenshot();
    } break;

    case ui::VirtualKey::kEscape: {
      // Allow users to escape fullscreen (but not enter it).
      if (!window_->IsFullscreen()) {
        return;
      }
      SetFullscreen(false);
    } break;

#ifdef DEBUG
    case ui::VirtualKey::kF7: {
      // Save to file
      // TODO: Choose path based on user input, or from options
      // TODO: Spawn a new thread to do this.
      emulator()->SaveToFile("test.sav");
    } break;
    case ui::VirtualKey::kF8: {
      // Restore from file
      // TODO: Choose path from user
      // TODO: Spawn a new thread to do this.
      emulator()->RestoreFromFile("test.sav");
    } break;
#endif  // #ifdef DEBUG

    case ui::VirtualKey::kPause: {
      CpuBreakIntoDebugger();
    } break;
    case ui::VirtualKey::kCancel: {
      CpuBreakIntoHostDebugger();
    } break;

    case ui::VirtualKey::kF1: {
      ShowFAQ();
    } break;

    case ui::VirtualKey::kF2: {
      ShowBuildCommit();
    } break;

    case ui::VirtualKey::kF9: {
      RunPreviouslyPlayedTitle();
    } break;

    default:
      return;
  }

  e.set_handled(true);
}

void EmulatorWindow::OnMouseDown(const ui::MouseEvent& e) {
  if (e.button() == ui::MouseEvent::Button::kRight) {
    // If menu is already open, close it instead of opening a new one
    if (context_menu_widget_qt_) {
      context_menu_widget_qt_->close();
      context_menu_widget_qt_ = nullptr;
      return;
    }

    // Show context menu on right-click
    auto* qt_window = dynamic_cast<ui::QtWindow*>(window_.get());
    if (qt_window) {
      // Create new menu widget each time - just like notification widget
      auto* context_menu = new ContextMenuWidgetQt(qt_window->qwindow());
      context_menu_widget_qt_ = context_menu;

      context_menu->AddAction(
          window_->IsFullscreen() ? "Exit Fullscreen" : "Fullscreen",
          [this]() { ToggleFullscreen(); });

      context_menu->AddSeparator();

      context_menu->AddAction("Post-Processing...",
                              [this]() { ToggleDisplayConfigDialog(); });

      // Get current vibration state
      bool vibration_enabled = false;
      auto input_sys = emulator()->input_system();
      if (input_sys) {
        vibration_enabled = input_sys->GetVibrationCvar();
      }

      QString vibration_text =
          QString("Vibration: %1").arg(vibration_enabled ? "On" : "Off");
      context_menu->AddAction(vibration_text,
                              [this]() { ToggleControllerVibration(); });

      context_menu->AddAction("Show Controller Hotkeys",
                              [this]() { DisplayHotKeysConfig(); });

      context_menu->AddSeparator();

      context_menu->AddAction("Take Screenshot",
                              [this]() { TakeScreenshot(); });

      context_menu->AddAction("Profiles Menu",
                              [this]() { ToggleProfilesConfigDialog(); });

      // Show menu at mouse position
      QPoint global_pos = QCursor::pos();
      context_menu->ShowAt(global_pos);
    }
  }
}

